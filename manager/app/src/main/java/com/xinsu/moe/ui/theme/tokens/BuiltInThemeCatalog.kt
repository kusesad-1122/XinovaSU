private fun provenance(
    filename: String,
    hash: String,
    primary: String,
    secondary: String,
    focus: String,
    surface: String,
) = ThemeProvenance(
    sourceFilename = filename,
    sha256 = hash,
    rightsStatus = ThemeRightsStatus.UserProvidedPendingPublicationConfirmation,
    roleNotes = mapOf(
        "primary" to primary,
        "secondary" to secondary,
        "focus" to focus,
        "surface" to surface,
    ),
    pipelineVersion = "1.0.0",
    contrastValidated = true,
)

