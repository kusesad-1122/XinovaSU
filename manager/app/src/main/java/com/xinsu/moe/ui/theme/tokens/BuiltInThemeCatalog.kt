        "sea-breeze-song",
        "pink-mist-night-window",
        "frost-white-crimson-eye",
        "blue-flame-cat-shadow",
    ).map(authoredThemes::getValue)

    /** Kept as an explicit pressure subset for targeted visual-regression checks. */
    val pressureThemes: List<ThemeTokenBundle> = all.filter {
        it.id in setOf("moonlit-silver-blue", "sakura-street-walk", "sea-breeze-song")
    }

    fun byId(id: String?): ThemeTokenBundle? = authoredThemes[id]
}

