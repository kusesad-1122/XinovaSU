object BuiltInThemeCatalog {
    private val authoredThemes = (
        curatedThemeBundles() + listOf(moonlitSilverBlue(), sakuraStreetWalk(), seaBreezeSong())
    ).associateBy(ThemeTokenBundle::id)

    /** The stable manifest order used by the gallery, tests and publication guard. */
    val all: List<ThemeTokenBundle> = listOf(
        "ink-white-companions",
